    record_agency_ledger_entries(&db, id)?;
    rebuild_agency_partner_entries(&db, id)?;
    append_audit_event(
        &db,
        actor_user_id,
        "agency",
        Some(id),
        "update_agency",
        Some(&session_token),
        None,
    )?;

    db.commit().map_err(|e| e.to_string())?;

    Ok(())
