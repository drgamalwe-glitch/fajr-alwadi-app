#[tauri::command]
pub fn delete_agency(
    state: State<AppState>,
    id: i64,
    expected_version: i64,
    session_token: String,
) -> Result<(), String> {
    let mut db_guard = state.db.lock().map_err(|e| e.to_string())?;
    let (db, actor_user_id) = begin_admin_transaction(&mut db_guard, &session_token)?;
    let (current_version, operation_id, account_id, payment_status): (i64, String, i64, String) =
        db.query_row(
            "SELECT version,operation_id,account_id,payment_status FROM agencies WHERE id=?1",
            [id],
            |row| Ok((row.get(0)?, row.get(1)?, row.get(2)?, row.get(3)?)),
        )
        .map_err(|_| "الوكالة المطلوبة غير موجودة أو تفتقد الهوية الرقمية".to_string())?;
    if current_version != expected_version {
        return Err(format!(
            "تعارض تعديل الوكالة: النسخة الحالية {} وليست {}",
            current_version, expected_version
        ));
    }
    if payment_status == "محذوفة" {
        return Err("الوكالة ملغاة مسبقًا".to_string());
    }
    let (reversal_date, _) = now_datetime();
    ensure_accounting_period_open(&db, &reversal_date)?;
    let cancellation_operation_id = new_ledger_token("agency_cancellation");
    db.execute(
        "INSERT INTO operations
         (id,operation_type,status,reverses_operation_id,actor_user_id,created_at)
         VALUES (?1,'agency_cancellation','active',?2,?3,
                 strftime('%Y-%m-%dT%H:%M:%fZ','now'))",
        params![cancellation_operation_id, operation_id, actor_user_id],
    )
    .map_err(|e| format!("تعذر إنشاء عملية إلغاء الوكالة: {e}"))?;

    let sources: Vec<(String, String, String)> = {
        let mut stmt = db
            .prepare(
                "SELECT DISTINCT source_type,source_id,source_role
                 FROM partner_transactions
                 WHERE operation_id=?1 OR operation_id IN (
                     SELECT operation_id FROM agency_transactions WHERE agency_id=?2)",
            )
            .map_err(|e| e.to_string())?;
        let rows = stmt
            .query_map(params![operation_id, id], |row| {
                Ok((row.get(0)?, row.get(1)?, row.get(2)?))
            })
            .map_err(|e| e.to_string())?
            .collect::<Result<Vec<_>, _>>()
            .map_err(|e| e.to_string())?;
        rows
    };
    for (source_type, source_id, source_role) in sources {
        append_partner_transaction_reversals_by_source(
            &db,
            &source_type,
            &source_id,
            &source_role,
            &cancellation_operation_id,
        )?;
    }
    reverse_ledger_entries(&db, "agency", &id.to_string())?;
    let tx_ids: Vec<i64> = {
        let mut stmt = db
            .prepare("SELECT id FROM agency_transactions WHERE agency_id=?1")
            .map_err(|e| e.to_string())?;
        let rows = stmt
            .query_map([id], |row| row.get(0))
            .map_err(|e| e.to_string())?
            .collect::<Result<Vec<_>, _>>()
            .map_err(|e| e.to_string())?;
        rows
    };
    for tx_id in tx_ids {
        reverse_ledger_entries(&db, "agency_transaction", &tx_id.to_string())?;
    }
    db.execute(
        "UPDATE financial_ledger SET operation_id=?1
         WHERE reverses_ledger_id IN (
             SELECT original.id FROM financial_ledger original
             WHERE original.operation_id=?2 OR original.operation_id IN (
                 SELECT operation_id FROM agency_transactions WHERE agency_id=?3))",
        params![cancellation_operation_id, operation_id, id],
    )
    .map_err(|e| format!("تعذر ربط قيود عكس الوكالة: {e}"))?;
    db.execute(
        "UPDATE operations
         SET status='reversed',reversed_at=strftime('%Y-%m-%dT%H:%M:%fZ','now'),
             reversal_operation_id=?1
         WHERE status='active' AND (id=?2 OR id IN (
             SELECT operation_id FROM agency_transactions WHERE agency_id=?3))",
        params![cancellation_operation_id, operation_id, id],
    )
    .map_err(|e| format!("تعذر عكس عمليات الوكالة: {e}"))?;
    let changed = db
        .execute(
            "UPDATE agencies SET payment_status='محذوفة',version=version+1,
             updated_at=strftime('%Y-%m-%dT%H:%M:%fZ','now')
             WHERE id=?1 AND version=?2",
            params![id, expected_version],
        )
        .map_err(|e| format!("تعذر إلغاء الوكالة: {e}"))?;
    if changed != 1 {
        return Err("تعارض تعديل الوكالة أثناء الإلغاء".to_string());
    }
    append_audit_event_with_details(
        &db,
        actor_user_id,
        "agency",
        Some(id),
        "cancel_agency",
        Some(&session_token),
        None,
        AuditEventDetails {
            operation_id: Some(&cancellation_operation_id),
            account_id: Some(account_id),
            reason: Some("إلغاء الوكالة مع حفظ التاريخ المالي"),
            ..Default::default()
        },
    )?;
    recalculate_all_partners(&db)?;
    db.commit().map_err(|e| e.to_string())?;
    Ok(())
}

