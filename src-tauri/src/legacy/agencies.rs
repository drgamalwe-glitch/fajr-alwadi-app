    db.execute(
        "UPDATE financial_ledger SET operation_id=?1
         WHERE (reference_type='agency_transaction' AND reference_entity_id=?2)
            OR (reference_type='partner_transaction' AND reference_entity_id IN (
                SELECT id FROM partner_transactions
                WHERE source_type='agency_transaction' AND source_entity_id=?2))",
        params![operation_id, tx_id],
    )
    .map_err(|e| format!("تعذر ربط قيود حركة الوكالة بالعملية: {e}"))?;
    append_audit_event_with_details(
        &db,
        actor_user_id,
        "agency_transaction",
        Some(tx_id),
        "create_agency_transaction",
        Some(&session_token),
        creation_token.as_deref(),
        AuditEventDetails {
            operation_id: Some(&operation_id),
            account_id: Some(account_id),
            ..Default::default()
        },
    )?;
