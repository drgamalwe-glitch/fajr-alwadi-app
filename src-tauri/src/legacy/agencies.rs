pub fn now_datetime() -> (String, String) {
    let now = Local::now();
    let date = now.format("%Y-%m-%d").to_string();
    let time = now.format("%H:%M").to_string();
    (date, time)
}

pub fn normalize_agency_payment_status(status: &str) -> &'static str {
    if status.trim() == "غير واصل" {
        "غير واصل"
    } else {
        "واصل"
    }
}

pub fn agency_is_received(status: &str) -> bool {
    normalize_agency_payment_status(status) == "واصل"
}

