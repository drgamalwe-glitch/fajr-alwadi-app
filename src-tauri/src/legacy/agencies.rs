#[allow(clippy::too_many_arguments)]
pub fn insert_agency_customer_receivable(
    db: &Connection,
    agency_id: i64,
    new_agent_name: &str,
    phone: &str,
    car_number: &str,
    amount: Money,
    currency: &str,
    date: &str,
    time: &str,
    agency_notes: &str,
) -> Result<(), String> {
    if amount <= Money::zero() {
        return Ok(());
    }

    let customer_name = new_agent_name.trim();
    let account_id = ensure_agency_account(db, customer_name, phone)?;
    db.execute(
        "UPDATE agencies SET account_id=COALESCE(account_id,?1) WHERE id=?2",
        params![account_id, agency_id],
    )
    .map_err(|e| format!("تعذر ربط الوكالة بهوية الحساب: {e}"))?;
    db.execute(
        "UPDATE financial_ledger SET account_id_v2=?1
         WHERE reference_type='agency' AND reference_entity_id=?2
           AND account_type='receivable'",
        params![account_id, agency_id],
    )
    .map_err(|e| format!("تعذر ربط قيد الوكالة بهوية الحساب: {e}"))?;
    let operation_id: Option<String> = db
        .query_row(
            "SELECT operation_id FROM agencies WHERE id=?1",
            [agency_id],
            |row| row.get(0),
        )
        .map_err(|e| format!("تعذر قراءة هوية عملية الوكالة: {e}"))?;

    let note = if agency_notes.trim().is_empty() {
        format!(
            "غير واصل وكالة {} رقم السيارة {}",
            customer_name,
            car_number.trim()
        )
    } else {
        format!(
            "غير واصل وكالة {} رقم السيارة {} - {}",
            customer_name,
            car_number.trim(),
            agency_notes.trim()
        )
    }
    .trim()
    .replace("  ", " ");

    db.execute(
        "INSERT INTO partner_transactions (
            partner_name, kind, type, amount, date, time, notes, currency, payment_type,
            source_type, source_id, source_entity_id, source_role,
            affects_qasa, affects_partner_cash, affects_profit,
            related_source_type, related_source_id, related_entity_id,
            account_id, operation_id
         )
         VALUES (?1, 'وكالة', 'باقي وكالة', ?2, ?3, ?4, ?5, ?6, 'قاصه',
            'agency', CAST(?7 AS TEXT), ?7, 'agency_receivable', 0, 0, 0,
            'agency', CAST(?7 AS TEXT), ?7, ?8, ?9)",
        params![
            customer_name,
            amount,
            date.trim(),
            time.trim(),
            note,
            currency,
            agency_id,
            account_id,
            operation_id.as_deref(),
        ],
    )
    .map_err(|e| e.to_string())?;

    recalculate_partner_total(db, customer_name, "وكالة")
}

#[allow(clippy::too_many_arguments)]
pub fn distribute_agency_partner_effects(
    db: &Connection,
    amount: Money,
    currency: &str,
    date: &str,
    note: &str,
    source_type: &str,
    source_id: &str,
    received: bool,
) -> Result<(), String> {
    // ============================================================
    // FORENSIC FIX (re-audit 2026-07-10, Instructions.md §31.4):
    // Agency profit recognition now depends on payment_status.
    //
    //   - Cash agency (received=true): profit_recognition + cash_movement
    //     are both created. Profit enters the Profit card, Qasa, and Cash.
    //
    //   - Credit agency (received=false): NO profit_recognition and NO
    //     cash_movement. Only a receivable row tracks the amount owed.
    //     Profit is deferred until the agency is marked "واصل" via
    //     set_agency_receivable_status, at which point both rows are
    //     created retroactively.
    //
    // This REPLACES the old §30.9 behavior which recognized profit
    // immediately regardless of payment status. The new rule is stricter:
    // no profit enters the system until real cash is received.
    // ============================================================
    if received {
        // Profit recognition (affects_profit=1, does NOT enter Qasa/Cash).
        distribute_to_partners_50_with_effects(
            db,
            amount,
            currency,
            date,
            "قاصه",
            "ايداع ارباح وكالة",
            note,
            source_type,
            source_id,
            "profit_recognition",
            false,
            false,
            true,
        )?;

        // Cash movement (affects_qasa=1, affects_partner_cash=1, NOT profit).
        distribute_to_partners_50_with_effects(
            db,
            amount,
            currency,
            date,
            "قاصه",
            "ايداع ارباح وكالة",
            note,
            source_type,
            source_id,
            "cash_movement",
            true,
            true,
            false,
        )?;
    }
    // If !received: NO profit_recognition, NO cash_movement.
    // The receivable row is created separately by the caller
    // (insert_agency_customer_receivable) and tracks the amount owed.
    Ok(())
}

